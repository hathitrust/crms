sub GetCountries
{
  return \%countries;
}

sub TranslateCountry
{
  my $code = shift;
  my $long = shift;

  $code =~ s/[^a-z]//gi;
  my $country = $countries{$code};
  $country = 'Undetermined' unless $country;
  $country =~ s/\s*\(.*?\)$//g unless $long;
  return $country;
}

